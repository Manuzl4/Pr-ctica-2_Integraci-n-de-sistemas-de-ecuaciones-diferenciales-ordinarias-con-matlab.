function dx=DPP(t,x)
%---------------Definicion de Parametros-------------%
m1=0.2;
m2=0.1;
l1=0.3;
l2=0.25;
g=9.81;
c=0.2;
%----------------------------------------------------%
dx=zeros(4,1);
%-------Definicion de la dinamica del Sistema--------%
dx(1)=x(2);
dx(2)=((l1*sin(x(3)-x(1))+g*sin(x(3)))*m2*cos(x(3)-x(1))+m2*l2*(x(4)^2)*sin(x(3)-x(1))-(m1+m2)*g*sin(x(1))-c*x(2))/((m1+m2)*l1-m2*l1*(cos(x(3)-x(1)))^2);
dx(3)=x(4);
dx(4)=((g*sin(x(1))+(c*x(2)-m2*l2*x(4)^2*sin(x(3)-x(1)))*(m1+m2)^(-1))*cos(x(3)-x(1))-l1*x(2)^2*sin(x(3)-x(1))-g*sin(x(3)))/(l2-(m2*l2*cos(x(3)-x(1))^2)*(m1+m2)^(-1));
%----------------------------------------------------%
end